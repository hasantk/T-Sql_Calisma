----------------------------DATETIME FONKSIYONLARI------------------------------------

--DATEADD
DECLARE @TARIH AS DATETIME
SET @TARIH='2019-01-01 16:21:37'
DECLARE @TARIH2 AS DATETIME
SET @TARIH2=DATEADD(DAY,3,@TARIH)
SELECT @TARIH,@TARIH2

--DATEDIFF
DECLARE @TARIH AS DATETIME
SET @TARIH='2019-01-01 16:21:37'
DECLARE @TARIH2 AS DATETIME
SET @TARIH2='2019-04-01 16:21:37'
SELECT DATEDIFF(MONTH,@TARIH,@TARIH2)

--DATEFROMPARTS
DECLARE @TARIH AS DATETIME
SET @TARIH=DATEFROMPARTS(2020,02,24)
SELECT @TARIH

--DATEPART
DECLARE @TARIH AS DATETIME
SET @TARIH='2019-01-14 16:21:37'
--SELECT DATEPART(DAY,@TARIH)
SELECT YEAR(@TARIH)

--GETDATE()
DECLARE @TARIH AS DATE
SET @TARIH='1990-12-04'
SELECT DATEDIFF(YEAR,@TARIH,GETDATE())